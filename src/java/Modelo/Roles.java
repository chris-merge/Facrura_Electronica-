package Modelo;

/**
 *
 * @author Gerardo
 */
public class Roles {
    private int idrol;
    private String rol;
    private String permisos;
    
    public Roles() {
    }
    
    public Roles(int idrol, String rol, String permisos) {
        this.idrol = idrol;
        this.rol = rol;
        this.permisos = permisos;
    }

    public int getIdrol() {
        return idrol;
    }

    public void setIdrol(int idrol) {
        this.idrol = idrol;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }

    public String getPermisos() {
        return permisos;
    }

    public void setPermisos(String permisos) {
        this.permisos = permisos;
    }
}
